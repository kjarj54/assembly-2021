.MODEL SMALL
.STACK 100H

.DATA

CHAR DB 00,'$'

POS_CURSOR MACRO FIL, COL
	MOV AH, 02H
	MOV BH, 00
	MOV DH, FIL
	MOV DL, COL
	INT 10H 
ENDM

.CODE
MAIN PROC FAR
	MOV AX, @DATA
	MOV DS, AX

	CALL LIMPIAR_PANTALLA 

	POS_CURSOR 0, 0

	CALL IMPRIMIR

	MOV AH, 00H
	INT 16H

	MOV AH, 4CH
	INT 21H
MAIN ENDP

LIMPIAR_PANTALLA PROC
	MOV AX, 0600H
	MOV BH, 17H
	MOV CX, 0000H
	MOV DX, 184FH
	INT 10H
	RET
LIMPIAR_PANTALLA ENDP

IMPRIMIR PROC
	MOV CX, 256
	LEA DX, CHAR
S20:
	CMP CHAR, 08H
	JB S30
	CMP CHAR, 0DH
	JBE S40
S30:
	MOV AH, 09H
	INT 21H
S40:
	INC CHAR
	LOOP S20
	RET
IMPRIMIR ENDP

END 

