.MODEL SMALL
.STACK 100H

.DATA

TEXTO DB 27 DUP ('$')

.CODE
MAIN PROC FAR
	MOV AX, @DATA
	MOV DS, AX

	XOR AX, AX
	MOV SI, OFFSET TEXTO
	MOV CX, 26D
	MOV AH, 41H

BUCLE:
	MOV [SI], AH
	INC AH
	INC SI
	LOOP BUCLE

	LEA  DX, TEXTO
	MOV AH, 09H
	INT 21H

	MOV AH, 4CH
	INT 21H

	RET
MAIN ENDP

END